#include "libft.h"

void *ft_safe_malloc(size_t size)
{
    void *ptr;

    ptr = malloc(size);
    if (!ptr)
    {
        write(2, "Memory allocation failed\n", 25);
        exit(EXIT_FAILURE);
    }
    return (ptr);
}
