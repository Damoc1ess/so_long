#include "libft.h"

int	ft_putnbr(int n)
{
	char	buffer[12];
	int		len;
	int		count;

	count = 0;
	len = 0;
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		ft_putchar('-');
		count++;
	}
	ft_itoa_base(n, buffer, &len);
	count += len;
	while (len > 0)
		ft_putchar(buffer[--len]);
	return (count);
}
